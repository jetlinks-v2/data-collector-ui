export const getState = (record: any) => {
    const enabled = record?.state?.value === 'enabled';
    if (record) {
        return {
            value: enabled ? record?.runningState?.value : 'processing',
            text: enabled ? record?.runningState?.text : i18n.global.t('Collector.data.400141-0'),
        };
    } else {
        return {};
    }
};

